function testSwitch()
{
    var nilai = 11;

    switch(nilai){
        case 10:
            console.log("Sangat Bagus");
            break;
            case 8:
                console.log("Baik");
                break;
                case 6:
                    console.log("cukup");
                    break;
                    default:
                        console.log("lainnya");
    }
}

testSwitch()